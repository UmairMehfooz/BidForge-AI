async def _understanding_text(workspace_name: str, requirements: list[dict]) -> str:
    """LLM-written 'Understanding of Requirements'; deterministic fallback."""
    by_type: dict[str, int] = {}
    for r in requirements:
        t = str(r.get("type") or "other")
        by_type[t] = by_type.get(t, 0) + 1
    type_summary = ", ".join(f"{v} {k}" for k, v in sorted(by_type.items()))

    fallback = (
        f"We have carefully reviewed the RFP '{workspace_name}', which comprises "
        f"{len(requirements)} extracted requirements ({type_summary}). Our response in "
        "the Technical Proposal section below addresses each mandatory requirement and "
        "question individually, citing evidence from our delivered-project record."
    )

    req_lines = "\n".join(
        f"- [{r.get('section_ref', 'General')}] {str(r.get('requirement', ''))[:200]}"
        for r in requirements[:40]
    )
    user = (
        f"RFP title: {workspace_name}\n"
        f"Total requirements extracted: {len(requirements)} ({type_summary})\n\n"
        f"Key requirements:\n{req_lines}\n\n"
        "Write the 'Understanding of Requirements' section of our proposal: 2-3 formal "
        "paragraphs showing the client we understood what they need — the project's purpose, "
        "scope, and critical success factors. Do NOT invent facts beyond the requirements "
        "listed. Do NOT add headings. Plain narrative text only."
    )
    return await _llm_text(
        "understanding_reqs",
        "You are a professional bid writer. You write formal, client-facing proposal prose. "
        "Never fabricate facts.",
        user, fallback,
    )


def _add_technical_proposal(doc, sections: list[dict], req_map: dict,
                            req_to_cap_name: dict,
                            ref_to_cap_names: dict | None = None):
    doc.add_heading("4. Technical Proposal", level=1)
    doc.add_paragraph(
        "Our response to each requirement of the RFP is set out below. Where our "
        "capability library provides direct evidence, the supporting project is cited; "
        "requirements without matching evidence are flagged explicitly."
    )

    from app.services.proposal_generator import section_sort_key
    sections = sorted(sections, key=lambda x: section_sort_key(x.get("section_ref")))

    for i, sec in enumerate(sections, start=1):
        title = sec.get("section_title") or f"Section {sec.get('section_ref', '')}"
        doc.add_heading(f"4.{i} {title}", level=2)

        req_id = sec.get("requirement_id")
        req_text = req_map.get(req_id)
        if req_text:
            p = doc.add_paragraph()
            run = p.add_run(f"Requirement: {req_text}")
            run.font.italic = True
            run.font.size = Pt(10)
            run.font.color.rgb = RGBColor(80, 80, 80)

        # Approved/edited text always wins over the raw AI draft.
        text = sec.get("edited_draft") or sec.get("ai_draft") or ""
        if text:
            doc.add_paragraph(text)

        cap_name = req_to_cap_name.get(req_id)
        if not cap_name and not req_id and ref_to_cap_names:
            # Legacy sections (saved before the requirement_id migration) can't
            # be joined 1:1 — cite the matched evidence for the whole RFP
            # section group instead of falsely flagging every one as a gap.
            group = ref_to_cap_names.get(str(sec.get("section_ref") or ""))
            if group:
                cap_name = ", ".join(group[:3])

        if cap_name:
            p = doc.add_paragraph()
            run = p.add_run(f"Supporting evidence: '{cap_name}' (capability library)")
            run.font.size = Pt(9)
            run.font.italic = True
            run.font.color.rgb = RGBColor(100, 100, 100)
        else:
            p = doc.add_paragraph()
            run = p.add_run("[GAP FLAGGED - No evidence found for this requirement]")
            run.bold = True
            run.font.color.rgb = RGBColor(192, 0, 0)


def _add_company_profile(doc, caps: list[dict]):
    """Deterministic profile aggregated from the capability library — no LLM,
    so nothing can be fabricated."""
    doc.add_heading("5. Company Profile & Credentials", level=1)

    domains = sorted({c.get("domain", "") for c in caps if c.get("domain")})
    certs = sorted({c.get("certification") for c in caps if c.get("certification")})
    years = [int(c["year_completed"]) for c in caps
             if str(c.get("year_completed") or "").isdigit()]
    client_types = sorted({c.get("client_type", "") for c in caps if c.get("client_type")})

    doc.add_paragraph(
        f"{COMPANY_NAME} has successfully delivered {len(caps)} documented projects"
        + (f" between {min(years)} and {max(years)}" if years else "")
        + f" across {len(domains)} practice areas, serving "
        + ", ".join(client_types).lower() + " clients."
    )

    doc.add_heading("Practice Areas", level=2)
    for d in domains:
        doc.add_paragraph(d, style="List Bullet")

    if certs:
        doc.add_heading("Certifications & Standards", level=2)
        for c in certs:
            doc.add_paragraph(c, style="List Bullet")


def _pick_top_projects(caps: list[dict], compliance_items: list[dict],
                       n: int = 3) -> list[dict]:
    """The n capabilities most frequently matched against this RFP's
    requirements; pad with the most recent projects if fewer were matched."""
    counts: dict[str, int] = {}
    for ci in compliance_items:
        cap_id = ci.get("matched_capability_id")
        if cap_id:
            counts[cap_id] = counts.get(cap_id, 0) + 1

    by_id = {c.get("id"): c for c in caps}
    picked = [by_id[cid] for cid, _ in
              sorted(counts.items(), key=lambda kv: -kv[1]) if cid in by_id][:n]

    if len(picked) < n:
        picked_ids = {c.get("id") for c in picked}
        rest = sorted(
            (c for c in caps if c.get("id") not in picked_ids),
            key=lambda c: -(int(c["year_completed"])
                            if str(c.get("year_completed") or "").isdigit() else 0),
        )
        picked.extend(rest[: n - len(picked)])
    return picked


