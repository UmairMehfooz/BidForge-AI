def _get_workspace_dir(workspace_id: str) -> Path:
    d = _BIDFORGE_TMP / workspace_id
    d.mkdir(parents=True, exist_ok=True)
    return d


async def _generate_exec_summary(workspace_name: str, proposal_text: str) -> str:
    """Generate a 1-paragraph executive summary using GROQ."""
    api_key = os.getenv("GROQ_API_KEY", "")
    if not api_key:
        return "Executive summary could not be generated (GROQ_API_KEY missing)."
        
    llm = ChatGroq(model=GROQ_MODEL, temperature=0.7, max_tokens=300, api_key=api_key)
    
    prompt = f"""You are a professional bid writer.
Write a single, compelling 1-paragraph executive summary for a proposal titled: "{workspace_name}".
Base it loosely on these proposal snippets:
{proposal_text[:2000]}

Do not include greetings. Just write the paragraph."""

    from app.utils.llm_cache import cached_invoke, demo_fallback

    messages = [
        SystemMessage(content="You write compelling executive summaries for IT proposals."),
        HumanMessage(content=prompt),
    ]
    try:
        # Cache-first (Fix 10) — this was the one LLM call site left unwired,
        # which is why exports said "Executive summary generation failed."
        return await cached_invoke("exec_summary", llm, messages)
    except Exception as exc:
        logger.error("Failed to generate exec summary: %s", exc)
        fallback = demo_fallback("exec_summary", str(messages[-1].content))
        if fallback:
            return fallback
        # Degrade to a deterministic, honest summary instead of an error line
        return (
            f"This proposal for '{workspace_name}' presents our company's relevant "
            "past-project experience, certifications, and delivery approach against "
            "each requirement of the RFP, as detailed in the narrative sections below."
        )


def _add_cover_page(doc, workspace_name: str):
    doc.add_paragraph()
    doc.add_paragraph()
    
    title = doc.add_paragraph(workspace_name)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in title.runs:
        run.font.size = Pt(28)
        run.bold = True
        run.font.color.rgb = RGBColor(0, 51, 102)

    doc.add_paragraph()
    
    subtitle = doc.add_paragraph("Technical & Commercial Proposal")
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in subtitle.runs:
        run.font.size = Pt(18)
        
    doc.add_paragraph("\n\n\n\n\n\n")
    
    info = doc.add_paragraph()
    info.alignment = WD_ALIGN_PARAGRAPH.CENTER
    info.add_run(f"Submitted by: {COMPANY_NAME}\n").bold = True
    info.add_run(f"Date: {datetime.now().strftime('%d %B %Y')}\n")
    
    doc.add_paragraph("\n\n\n\n")
    footer = doc.add_paragraph("Prepared by BidForge AI")
    footer.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in footer.runs:
        run.font.size = Pt(10)
        run.font.color.rgb = RGBColor(128, 128, 128)
        
    doc.add_page_break()


def _add_exec_summary(doc, summary_text: str):
    doc.add_heading("1. Executive Summary", level=1)
    doc.add_paragraph(summary_text)
    doc.add_page_break()


def _add_proposal_sections(doc, sections: list[dict], capabilities_map: dict):
    doc.add_heading("2. Proposal Narrative", level=1)
    
    # Natural sort ("2" before "10", letters after numbers, General last) —
    # plain string sort put Section 10 before Section 4.
    from app.services.proposal_generator import section_sort_key
    sections.sort(key=lambda x: section_sort_key(x.get("section_ref")))
    
    for sec in sections:
        title = sec.get("section_title") or f"Section {sec.get('section_ref', '')}"
        doc.add_heading(title, level=2)
        
        text = sec.get("edited_draft") or sec.get("ai_draft") or ""
        doc.add_paragraph(text)
        
        # Source line
        req_id = sec.get("requirement_id")
        if req_id in capabilities_map:
            cap_name = capabilities_map[req_id]
            p = doc.add_paragraph()
            run = p.add_run(f"Source: Derived from past experience in '{cap_name}'")
            run.font.size = Pt(9)
            run.font.italic = True
            run.font.color.rgb = RGBColor(100, 100, 100)


def _add_compliance_checklist(doc, compliance_items: list[dict], req_map: dict):
    doc.add_page_break()
    doc.add_heading("3. Compliance Checklist", level=1)
    
    table = doc.add_table(rows=1, cols=3)
    table.style = 'Light Shading Accent 1'
    
    hdr_cells = table.rows[0].cells
    hdr_cells[0].text = 'Requirement'
    hdr_cells[1].text = 'Status'
    hdr_cells[2].text = 'Gap Note'
    
    for ci in compliance_items:
        req_id = ci.get("requirement_id")
        req_text = req_map.get(req_id, "Unknown Requirement")
        status = ci.get("status", "fail").lower()
        note = ci.get("gap_note") or ""
        
        row_cells = table.add_row().cells
        row_cells[0].text = req_text
        
        if status == "pass":
            row_cells[1].text = "✅ PASS"
        elif status == "partial":
            row_cells[1].text = "⚠️ PARTIAL"
        else:
            row_cells[1].text = "❌ FAIL"
            
        row_cells[2].text = note


def _add_win_score(doc, score: dict):
    doc.add_page_break()
    doc.add_heading("4. BidForge AI Win Score Analysis", level=1)
    
    overall = score.get("overall_score_pct", 0)
    decision = score.get("decision", "NO-GO")
    
    marker = "✅" if decision == "GO" else ("⚠️" if decision == "CONDITIONAL" else "❌")
    
    doc.add_paragraph(f"Overall Score: {overall}%\nDecision: {decision} {marker}", style='Intense Quote')
    
    bd = score.get("score_breakdown", {}).get("raw_scores", {})
    if bd:
        doc.add_heading("Score Breakdown", level=2)
        table = doc.add_table(rows=1, cols=2)
        table.style = 'Light List Accent 1'
        table.rows[0].cells[0].text = 'Criterion'
        table.rows[0].cells[1].text = 'Score'
        
        for k, v in bd.items():
            row_cells = table.add_row().cells
            row_cells[0].text = k.replace("_", " ").title()
            row_cells[1].text = f"{v*100:.1f}%"


async def generate_docx_export(workspace_id: str) -> str:
    """
    Generate the final DOCX proposal.
    Returns the absolute file path to the generated DOCX.
    """
    sb = get_supabase()
    
    # ── Load Data ────────────────────────────────────────────────────────────
    ws_res = sb.table("workspaces").select("name").eq("id", workspace_id).single().execute()
    if not ws_res.data:
        raise HTTPException(status.HTTP_404_NOT_FOUND, "Workspace not found")
    workspace_name = ws_res.data.get("name", "Proposal")
    
    sec_res = sb.table("proposal_sections").select("*").eq("workspace_id", workspace_id).execute()
    sections = sec_res.data or []
    
    req_res = sb.table("requirements").select("*").eq("workspace_id", workspace_id).execute()
    requirements = req_res.data or []
    req_map = {r["id"]: r.get("requirement", "") for r in requirements}
    
    comp_res = sb.table("compliance_items").select("*").eq("workspace_id", workspace_id).execute()
    compliance_items = comp_res.data or []
    
    # Canonical table is bid_scores (Fix 12); legacy fallback until renamed.
    try:
        score_res = sb.table("bid_scores").select("*").eq("workspace_id", workspace_id).execute()
    except Exception:
        score_res = sb.table("win_scores").select("*").eq("workspace_id", workspace_id).execute()
        logger.warning("Read score from LEGACY win_scores table — run: "
                       "ALTER TABLE win_scores RENAME TO bid_scores;")
    score = score_res.data[0] if score_res.data else {}
    
    caps = get_all_capabilities()
    cap_map = {c["id"]: c.get("project_title", "") for c in caps}
    
    # Map requirement -> matched capability project name
    req_to_cap_name = {}
    for ci in compliance_items:
        cap_id = ci.get("matched_capability_id")
        if cap_id and cap_id in cap_map:
            req_to_cap_name[ci["requirement_id"]] = cap_map[cap_id]
            
    # ── Exec Summary Gen ─────────────────────────────────────────────────────
    combined_draft = " ".join((s.get("edited_draft") or s.get("ai_draft") or "") for s in sections)
    exec_summary = await _generate_exec_summary(workspace_name, combined_draft)

    # ── Build Document ───────────────────────────────────────────────────────
    try:
        from docx import Document
    except ImportError:
        raise HTTPException(status.HTTP_500_INTERNAL_SERVER_ERROR, "python-docx not installed")
        
    doc = Document()
    
    _add_cover_page(doc, workspace_name)
    _add_exec_summary(doc, exec_summary)
    _add_proposal_sections(doc, sections, req_to_cap_name)
    _add_compliance_checklist(doc, compliance_items, req_map)
    _add_win_score(doc, score)
    
    # ── Save Document ────────────────────────────────────────────────────────
    # Unique filename per export — a fixed name hits Windows file locks when
    # the previous copy is still open/being streamed, making re-export hang.
    save_dir = _get_workspace_dir(workspace_id)
    stamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    save_path = save_dir / f"proposal_draft_{stamp}.docx"

    doc.save(str(save_path))

    logger.info("Generated DOCX for workspace=%s at %s", workspace_id, save_path)
    return str(save_path)


