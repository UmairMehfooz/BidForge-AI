async def _llm_text(tag: str, system: str, user: str, fallback_text: str,
                    max_tokens: int = 700) -> str:
    """One cached LLM call with provider rotation; degrades to fallback_text."""
    api_key = os.getenv("GROQ_API_KEY", "")
    if not api_key:
        return fallback_text

    from app.utils.llm_cache import cached_invoke, demo_fallback

    llm = ChatGroq(model=GROQ_MODEL, temperature=0.6, max_tokens=max_tokens, api_key=api_key)
    messages = [SystemMessage(content=system), HumanMessage(content=user)]
    try:
        return await cached_invoke(tag, llm, messages)
    except Exception as exc:
        logger.error("LLM call failed for full-proposal tag '%s': %s", tag, exc)
        fb = demo_fallback(tag, user)
        return fb or fallback_text


