def _add_compliance_matrix(doc, compliance_items: list[dict], req_map: dict,
                           cap_map: dict):
    doc.add_page_break()
    doc.add_heading("7. Compliance Matrix", level=1)

    table = doc.add_table(rows=1, cols=3)
    table.style = "Light Shading Accent 1"
    hdr = table.rows[0].cells
    hdr[0].text = "Requirement"
    hdr[1].text = "Compliant"
    hdr[2].text = "Evidence"

    for ci in compliance_items:
        req_text = req_map.get(ci.get("requirement_id"), "Unknown Requirement")
        st = str(ci.get("status", "fail")).lower()
        cap_title = cap_map.get(ci.get("matched_capability_id") or "")

        row = table.add_row().cells
        row[0].text = req_text
        row[1].text = "Yes" if st == "pass" else ("Partial" if st == "partial" else "No")
        row[2].text = (
            cap_title or ci.get("gap_note")
            or "[GAP FLAGGED - No evidence found for this requirement]"
        )


