async def generate_full_proposal_docx(workspace_id: str) -> str:
    """
    Generate the complete 8-section proposal document.
    Returns the absolute file path to the generated DOCX.
    """
    sb = get_supabase()

    ws_res = sb.table("workspaces").select("name").eq("id", workspace_id).single().execute()
    if not ws_res.data:
        raise HTTPException(status.HTTP_404_NOT_FOUND, "Workspace not found")
    workspace_name = ws_res.data.get("name", "Proposal")

    sections = (sb.table("proposal_sections").select("*")
                .eq("workspace_id", workspace_id).execute()).data or []
    requirements = (sb.table("requirements").select("*")
                    .eq("workspace_id", workspace_id).execute()).data or []
    req_map = {r["id"]: r.get("requirement", "") for r in requirements}
    compliance_items = (sb.table("compliance_items").select("*")
                        .eq("workspace_id", workspace_id).execute()).data or []

    if not sections:
        raise HTTPException(
            status.HTTP_422_UNPROCESSABLE_ENTITY,
            "No proposal sections found — generate the proposal draft first.",
        )

    caps = get_all_capabilities()
    cap_map = {c["id"]: c.get("project_title", "") for c in caps}
    req_to_cap_name = {
        ci["requirement_id"]: cap_map[ci["matched_capability_id"]]
        for ci in compliance_items
        if ci.get("matched_capability_id") in cap_map
    }

    # Evidence grouped by RFP section ref — fallback for legacy proposal
    # sections that were saved without a requirement_id.
    req_ref_of = {r["id"]: str(r.get("section_ref") or "") for r in requirements}
    ref_to_cap_names: dict[str, list[str]] = {}
    for req_id, cap_name in req_to_cap_name.items():
        ref = req_ref_of.get(req_id)
        if ref and cap_name not in ref_to_cap_names.setdefault(ref, []):
            ref_to_cap_names[ref].append(cap_name)

    # Narrative pieces (LLM, cached, provider-rotated; deterministic fallback)
    combined_draft = " ".join(
        (s.get("edited_draft") or s.get("ai_draft") or "") for s in sections
    )
    exec_summary = await _generate_exec_summary(workspace_name, combined_draft)
    understanding = await _understanding_text(workspace_name, requirements)
    top_projects = _pick_top_projects(caps, compliance_items, n=3)

    # ── Build Document ───────────────────────────────────────────────────────
    doc = Document()

    _add_full_cover_page(doc, workspace_name)

    doc.add_heading("2. Executive Summary", level=1)
    doc.add_paragraph(exec_summary)
    doc.add_page_break()

    doc.add_heading("3. Understanding of Requirements", level=1)
    doc.add_paragraph(understanding)
    doc.add_page_break()

    _add_technical_proposal(doc, sections, req_map, req_to_cap_name, ref_to_cap_names)
    doc.add_page_break()

    _add_company_profile(doc, caps)
    doc.add_page_break()

    await _add_past_projects(doc, top_projects)
    _add_compliance_matrix(doc, compliance_items, req_map, cap_map)
    _add_appendices(doc)

    # ── Save ─────────────────────────────────────────────────────────────────
    save_dir = _get_workspace_dir(workspace_id)
    stamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    save_path = save_dir / f"full_proposal_{stamp}.docx"
    doc.save(str(save_path))

    logger.info("Generated FULL proposal DOCX for workspace=%s at %s",
                workspace_id, save_path)
    return str(save_path)
